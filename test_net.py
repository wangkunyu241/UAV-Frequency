if __name__ == '__main__':

  args = parse_args()

  print('Called with args:')
  print(args)

  if torch.cuda.is_available() and not args.cuda:
    print("WARNING: You have a CUDA device, so you should probably run with --cuda")

  torch.manual_seed(cfg.RNG_SEED)
  torch.cuda.manual_seed_all(cfg.RNG_SEED)
  np.random.seed(cfg.RNG_SEED)

  args.imdbval_name1= "uav_2017_test_dark"
  args.imdbval_name2 = "uav_2017_test_haze"
  args.imdbval_name3 = "visdrone_2017_test_illumination"
  args.set_cfgs = ['ANCHOR_SCALES', '[1, 2, 4, 8, 16]', 'ANCHOR_RATIOS', '[0.5,1,2]', 'MAX_NUM_GT_BOXES', '107']

  args.cfg_file = "cfgs/{}_ls.yml".format(args.net) if args.large_scale else "cfgs/{}.yml".format(args.net)

  if args.cfg_file is not None:
    cfg_from_file(args.cfg_file)
  if args.set_cfgs is not None:
    cfg_from_list(args.set_cfgs)

  print('Using config:')
  pprint.pprint(cfg)

  cfg.TRAIN.USE_FLIPPED = False
  imdb1, roidb1, ratio_list1, ratio_index1 = combined_roidb(args.imdbval_name1, False)
  imdb1.competition_mode(on=True)
  print('{:d} roidb1 entries'.format(len(roidb1)))
  imdb2, roidb2, ratio_list2, ratio_index2 = combined_roidb(args.imdbval_name2, False)
  imdb2.competition_mode(on=True)
  print('{:d} roidb2 entries'.format(len(roidb2)))
  imdb3, roidb3, ratio_list3, ratio_index3 = combined_roidb(args.imdbval_name3, False)
  imdb3.competition_mode(on=True)
